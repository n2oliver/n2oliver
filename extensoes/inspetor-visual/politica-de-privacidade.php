<p>
O <strong>Inspetor Visual</strong> respeita a privacidade dos usuários.
Esta extensão foi desenvolvida para auxiliar na inspeção de elementos de páginas da web e na leitura em voz alta de conteúdos exibidos na tela, sem coletar, armazenar ou compartilhar informações pessoais.
</p>
</section>

<section>

<h2>Informações acessadas</h2>

<p>
Durante o uso, a extensão pode acessar temporariamente:
</p>

<ul>
<li>A estrutura HTML (DOM) da página aberta pelo usuário.</li>
<li>Os estilos (CSS) aplicados aos elementos da página.</li>
<li>O texto dos elementos selecionados para utilização da função de leitura em voz alta.</li>
</ul>

<p>
Essas informações são utilizadas apenas enquanto a extensão está em funcionamento e exclusivamente mediante ação do próprio usuário.
</p>

</section>

<section>

<h2>Coleta e armazenamento de dados</h2>

<p>O Inspetor Visual:</p>

<ul>
<li>Não coleta dados pessoais.</li>
<li>Não armazena informações de navegação.</li>
<li>Não registra histórico de sites visitados.</li>
<li>Não envia dados para servidores externos.</li>
<li>Não compartilha informações com terceiros.</li>
<li>Não utiliza dados para publicidade, marketing ou criação de perfis de usuários.</li>
</ul>

</section>

<section>

<h2>Uso dos dados</h2>

<p>
Todo o processamento ocorre localmente no navegador do usuário e possui como única finalidade fornecer os recursos da extensão, incluindo:
</p>

<ul>
<li>Exibir informações sobre elementos HTML.</li>
<li>Exibir estilos CSS dos elementos.</li>
<li>Permitir a cópia das informações solicitadas pelo usuário.</li>
<li>Ler em voz alta o texto do elemento selecionado.</li>
</ul>

</section>

<section>

<h2>Permissões</h2>

<p>
As permissões solicitadas pela extensão são utilizadas exclusivamente para possibilitar o funcionamento de seus recursos. Nenhuma permissão é utilizada para monitorar, coletar, armazenar ou transmitir dados pessoais.
</p>

</section>

<section>

<h2>Segurança</h2>

<p>
Como o Inspetor Visual não coleta nem armazena dados pessoais em servidores, não existe banco de dados de usuários relacionado ao uso da extensão.
</p>

</section>

